/**
 * 量词默认是贪婪匹配，即尽可能多的重复
 * 量词后加'?'后表示开启惰性匹配，即尽可能少的重复
 */

// 示例
const reg = /\d{2,5}/g;
const str = '1, 12, 123, 1234, 12345, 123456';
console.log(str.match(reg)) // [ '12', '123', '1234', '12345', '12345' ]
const reg2 = /\d{2,5}?/g
console.log(str.match(reg2)); //['12', '12', '12','34', '12', '34','12', '34', '56']

